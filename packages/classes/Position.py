class Position:
    ###############################################################
    ########################## __SLOTS__ ##########################
    ###############################################################
    __slots__ = (
        "_x",
        "_y"
    )

    ###############################################################
    ############################ HINTS ############################
    ###############################################################
    _x: int
    _y: int

    ###############################################################
    ########################### __INIT__ ##########################
    ###############################################################
    def __init__(self, x: int, y: int):
        self.set_x(x)
        self.set_y(y)

    ###############################################################
    ########################### GETTERS ###########################
    ###############################################################
    def get_x(self) -> int:
        return self._x

    def get_y(self) -> int:
        return self._y

    ###############################################################
    ########################### SETTERS ###########################
    ###############################################################
    def set_x(self, x: int) -> None:
        if isinstance(x, int):
            self._x = x
        else:
            raise Exception(
                "Error: Impossible to set x for a " + type(self).__name__ + ":" +
                "\nx is an int, but a " + type(x).__name__ + " is given."
            )

    def set_y(self, y: int) -> None:
        if isinstance(y, int):
            self._y = y
        else:
            raise Exception(
                "Error: Impossible to set y for a " + type(self).__name__ + ":" +
                "\ny is an int, but a " + type(y).__name__ + " is given."
            )

    ###############################################################
    ########################### __STR__ ###########################
    ###############################################################
    def __str__(self) -> str:
        return "x = " + str(self.get_x()) + "; y = " + str(self.get_y())
